package byow.Core;

public class RoomCoordinates {
    int startx;
    int starty;
    int endx;
    int endy;
    int roomw;
    int rooml;
    int randomx;
    int randomy;


    public RoomCoordinates(int sx,int sy,int ex,int ey,int rw,int rl,int rx,int ry){
        startx = sx;
        starty = sy;
        endx = ex;
        endy = ey;
        roomw = rw;
        rooml = rl;
        randomx = rx;
        randomy = ry;

    }



}
